package org.example;

// Define a utility class called GreetingUtility
public class GreetingUtility {

    // Define a method called welcomeUser which takes a String parameter (userName)
    public static void welcomeUser(String userName) {
        // Print a welcome message with the user's name
        System.out.println("Hello, " + userName + "! Welcome to the world of programming.");
    }

    // Define a method called farewellUser which takes a String parameter (userName)
    public static void farewellUser(String userName) {
        // Print a farewell message with the user's name
        System.out.println("Goodbye, " + userName + "! We hope to see you again soon.");
    }
}
